contador = 10
while contador > 0:
    print(contador)
    contador -= 1
else:
    print("Fim da contagem regressiva!")

# O algoritmo inicia com uma variável com valor inicial de 10, que é subtraída em 1, 
# enquanto a mesma for maior que 0, ao final da repetição é exibido a mensagem 
# "Fim da contagem regressiva!".