#Faça um programa que leia a largura, altura e calcule a área do triângulo.

print('Vamos calcular a área de um triângulo!')
largura = float(input('Qual a largura do triângulo em centímetros? '))
altura = float(input('Qual a altura do triângulo em centímetros? '))
área = (largura * altura) / 2
print(f'A área do triângulo mede {área} centímetros')